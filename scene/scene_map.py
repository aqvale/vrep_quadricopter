class SceneMap():
    xMin = None
    xMax = None
    yMin = None
    yMax = None
    zMin = None
    zMax = None

    def __init__(self, xMin, xMax, yMin, yMax, zMin, zMax):
        self.xMin = xMin
        self.xMax = xMax
        self.yMin = yMin
        self.yMax = yMax
        self.zMin = zMin
        self.zMax = zMax